const deviceSelect = document.querySelector("#device-model");
const languageSelect = document.querySelector("#language-select");
const dimensions = document.querySelector("#dimensions");
const selectedDeviceMeta = document.querySelector("#selected-device-meta");
const previewFrame = document.querySelector("#phone-preview");
const previewImage = document.querySelector("#wallpaper-preview");
const previewMessage = document.querySelector("#preview-message");
const previewModeButtons = document.querySelectorAll("[data-preview-mode]");
const lockDate = document.querySelector("#lock-date");
const lockTime = document.querySelector("#lock-time");
const urlInput = document.querySelector("#wallpaper-url");
const copyButton = document.querySelector("#copy-url");
const openLink = document.querySelector("#open-wallpaper");
const downloadLink = document.querySelector("#download-wallpaper");
const metaDescription = document.querySelector("#meta-description");

let presets = [];
let locale = preferredLocale();
let previewMessageKey = "preparing";
let copyResetTimer;

function preferredLocale() {
  let saved;
  try {
    saved = localStorage.getItem("calendar-wallpaper-language");
  } catch {
    saved = null;
  }
  const requested = saved || navigator.languages?.[0] || navigator.language || "en";
  const normalized = requested.toLowerCase();
  if (normalized.startsWith("zh-tw") || normalized.startsWith("zh-hk") || normalized.startsWith("zh-hant")) return "zh-Hant";
  if (normalized.startsWith("zh")) return "zh-Hans";
  if (normalized.startsWith("ja")) return "ja";
  if (normalized.startsWith("es")) return "es";
  return "en";
}

function text(key) {
  return translations[locale][key] || translations.en[key] || key;
}

function setPreviewMessage(key) {
  previewMessageKey = key;
  previewMessage.textContent = text(key);
}

function applyLanguage() {
  document.documentElement.lang = locale;
  document.title = text("pageTitle");
  metaDescription.content = text("description");
  languageSelect.value = locale;
  languageSelect.setAttribute("aria-label", text("language"));
  previewImage.alt = text("previewAlt");

  for (const element of document.querySelectorAll("[data-i18n]")) {
    element.textContent = text(element.dataset.i18n);
  }
  for (const element of document.querySelectorAll("[data-i18n-html]")) {
    element.innerHTML = text(element.dataset.i18nHtml);
  }
  for (const element of document.querySelectorAll("[data-i18n-aria-label]")) {
    element.setAttribute("aria-label", text(element.dataset.i18nAriaLabel));
  }
  const earlierGroup = deviceSelect.querySelector('optgroup[data-group="earlier"]');
  if (earlierGroup) earlierGroup.label = text("earlierModels");
  updateLockScreenClock();
  updateDeviceMeta(selectedPreset());
  if (!previewMessage.hidden) setPreviewMessage(previewMessageKey);
}

function updateLockScreenClock() {
  const now = new Date();
  lockDate.textContent = new Intl.DateTimeFormat(locale, {
    weekday: "long",
    month: "long",
    day: "numeric"
  }).format(now);
  lockTime.textContent = new Intl.DateTimeFormat(locale, {
    hour: "2-digit",
    minute: "2-digit",
    hour12: false
  }).format(now);
}

function shanghaiDateKey() {
  const parts = new Intl.DateTimeFormat("en-CA", {
    timeZone: "Asia/Shanghai",
    year: "numeric",
    month: "2-digit",
    day: "2-digit"
  }).formatToParts(new Date());
  const values = Object.fromEntries(parts.map(({ type, value }) => [type, value]));
  return `${values.year}-${values.month}-${values.day}`;
}

function wallpaperPath(preset) {
  return `output/devices/${preset.id}/today.png`;
}

function selectedPreset() {
  return presets.find((preset) => preset.id === deviceSelect.value) || presets[0];
}

function cutoutLabel(cutout) {
  if (cutout === "dynamic-island") return text("dynamicIsland");
  if (cutout === "notch") return text("notch");
  return text("noCutout");
}

function updateDeviceMeta(preset) {
  if (!preset) return;
  selectedDeviceMeta.textContent = `${preset.width} × ${preset.height} · ${cutoutLabel(preset.cutout)}`;
}

function enableLink(link, href) {
  link.href = href;
  link.removeAttribute("aria-disabled");
}

function updateSelection() {
  const preset = selectedPreset();
  if (!preset) return;

  const path = wallpaperPath(preset);
  const stableUrl = new URL(path, document.baseURI).href;
  dimensions.textContent = `${preset.width} × ${preset.height}`;
  updateDeviceMeta(preset);
  previewFrame.style.setProperty("--preview-ratio", `${preset.width} / ${preset.height}`);
  previewFrame.dataset.cutout = preset.cutout;
  previewImage.classList.remove("is-ready");
  previewMessage.hidden = false;
  setPreviewMessage("loadingWallpaper");
  previewImage.src = `${path}?date=${shanghaiDateKey()}`;
  urlInput.value = stableUrl;
  copyButton.disabled = false;
  enableLink(openLink, stableUrl);
  enableLink(downloadLink, stableUrl);
  downloadLink.download = `calendar-wallpaper-${preset.id}.png`;
}

function populateDevices() {
  deviceSelect.innerHTML = "";
  const deviceModels = presets.flatMap((preset) => (
    preset.models.map((model) => ({ model, presetId: preset.id }))
  ));
  const groups = ["17", "16", "15", "14", "13", "12", "11", "earlier"];

  for (const group of groups) {
    const entries = deviceModels
      .filter(({ model }) => deviceGroup(model) === group)
      .sort((a, b) => modelRank(a.model) - modelRank(b.model));
    if (entries.length === 0) continue;

    const optionGroup = document.createElement("optgroup");
    optionGroup.dataset.group = group;
    optionGroup.label = group === "earlier" ? text("earlierModels") : `iPhone ${group}`;

    for (const { model, presetId } of entries) {
      const option = document.createElement("option");
      option.value = presetId;
      option.dataset.model = model;
      option.textContent = model;
      if (model === "iPhone 17 Pro") option.selected = true;
      optionGroup.append(option);
    }
    deviceSelect.append(optionGroup);
  }
  deviceSelect.disabled = false;
  updateSelection();
}

function deviceGroup(model) {
  if (model === "iPhone Air") return "17";
  return model.match(/^iPhone (17|16|15|14|13|12|11)/)?.[1] || "earlier";
}

function modelRank(model) {
  if (model.includes("Pro Max")) return 0;
  if (model.endsWith(" Pro")) return 1;
  if (model === "iPhone Air") return 2;
  if (model.includes("Plus")) return 3;
  if (/^iPhone \d+$/.test(model)) return 4;
  if (model.includes("mini")) return 5;
  return 10;
}

previewImage.addEventListener("load", () => {
  previewImage.classList.add("is-ready");
  previewMessage.hidden = true;
});

previewImage.addEventListener("error", () => {
  previewImage.classList.remove("is-ready");
  previewMessage.hidden = false;
  setPreviewMessage("wallpaperPending");
});

deviceSelect.addEventListener("change", updateSelection);

for (const button of previewModeButtons) {
  button.addEventListener("click", () => {
    const isLockScreen = button.dataset.previewMode === "lockscreen";
    previewFrame.classList.toggle("is-lockscreen", isLockScreen);
    for (const modeButton of previewModeButtons) {
      const isActive = modeButton === button;
      modeButton.classList.toggle("is-active", isActive);
      modeButton.setAttribute("aria-pressed", String(isActive));
    }
  });
}

languageSelect.addEventListener("change", () => {
  locale = languageSelect.value;
  try {
    localStorage.setItem("calendar-wallpaper-language", locale);
  } catch {
    // The selected language still applies for this page load.
  }
  applyLanguage();
});

copyButton.addEventListener("click", async () => {
  try {
    await navigator.clipboard.writeText(urlInput.value);
  } catch {
    urlInput.select();
    document.execCommand("copy");
    urlInput.setSelectionRange(0, 0);
  }
  clearTimeout(copyResetTimer);
  copyButton.textContent = text("copied");
  copyResetTimer = setTimeout(() => {
    copyButton.textContent = text("copyUrl");
  }, 1600);
});

applyLanguage();
setInterval(updateLockScreenClock, 60_000);

fetch("device-presets.json")
  .then((response) => {
    if (!response.ok) throw new Error(`Device list request failed: ${response.status}`);
    return response.json();
  })
  .then((data) => {
    presets = Array.isArray(data.presets) ? data.presets : [];
    if (presets.length === 0) throw new Error("No device presets are configured");
    populateDevices();
  })
  .catch(() => {
    deviceSelect.innerHTML = `<option>${text("devicesUnavailable")}</option>`;
    dimensions.textContent = text("unavailable");
    setPreviewMessage("deviceLoadError");
  });
